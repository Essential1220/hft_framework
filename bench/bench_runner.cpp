std::string emit_markdown(const std::vector<BenchResult>& results,
                          const std::string& machine_info) {
    std::ostringstream out;
    out << "# hft_framework Latency Benchmark\n\n";
    out << "Auto-generated by `hft_bench`. Reproduce with:\n\n";
    out << "```\nhft_bench --output docs/BENCHMARK.md --runs 5 --warmup 1\n```\n\n";
    out << "## Machine\n\n```\n" << machine_info << "```\n\n";

    bool has_latency = false;
    bool has_throughput = false;
    for (const auto& r : results) {
        if (r.is_throughput) has_throughput = true;
        else has_latency = true;
    }

    if (has_latency) {
        out << "## Hot-path latency (lower is better)\n\n";
        out << "| Scenario | n | p50 | p95 | p99 | p99.9 | max | avg |\n";
        out << "|---|---:|---:|---:|---:|---:|---:|---:|\n";
        for (const auto& r : results) {
            if (r.is_throughput) continue;
            out << "| `" << r.name << "` | " << r.n << " | ";
            format_us(out, r.latency.p50_us); out << "us | ";
            format_us(out, r.latency.p95_us); out << "us | ";
            format_us(out, r.latency.p99_us); out << "us | ";
            format_us(out, r.latency.p999_us); out << "us | ";
            format_us(out, r.latency.max_us); out << "us | ";
            format_us(out, r.latency.avg_us); out << "us |\n";
        }
        out << "\n";
    }

    if (has_throughput) {
        out << "## Throughput (higher is better)\n\n";
        out << "| Scenario | n | ops/sec |\n|---|---:|---:|\n";
        for (const auto& r : results) {
            if (!r.is_throughput) continue;
            out << "| `" << r.name << "` | " << r.n << " | "
                << std::scientific << std::setprecision(3) << r.throughput_ops_sec
                << " |\n";
        }
        out << "\n";
    }

    return out.str();
}

std::string emit_csv(const std::vector<BenchResult>& results) {
    std::ostringstream out;
    out << "name,is_throughput,n,p50_us,p95_us,p99_us,p999_us,max_us,avg_us,ops_per_sec\n";
    out << std::fixed << std::setprecision(4);
    for (const auto& r : results) {
        out << r.name << "," << (r.is_throughput ? 1 : 0) << "," << r.n
            << "," << r.latency.p50_us << "," << r.latency.p95_us
            << "," << r.latency.p99_us << "," << r.latency.p999_us
            << "," << r.latency.max_us << "," << r.latency.avg_us
            << "," << r.throughput_ops_sec << "\n";
    }
    return out.str();
}

std::string emit_json(const std::vector<BenchResult>& results,
                      const std::string& machine_info) {
    auto escape = [](const std::string& s) {
        std::string out;
        out.reserve(s.size() + 8);
        for (char c : s) {
            switch (c) {
                case '"':  out += "\\\""; break;
                case '\\': out += "\\\\"; break;
                case '\n': out += "\\n";  break;
                case '\r': out += "\\r";  break;
                case '\t': out += "\\t";  break;
                default:   out += c;
            }
        }
        return out;
    };

    std::ostringstream out;
    out << std::fixed << std::setprecision(4);
    out << "{\n  \"machine\": \"" << escape(machine_info) << "\",\n";
    out << "  \"results\": [\n";
    for (size_t i = 0; i < results.size(); ++i) {
        const auto& r = results[i];
        out << "    {\"name\": \"" << escape(r.name)
            << "\", \"is_throughput\": " << (r.is_throughput ? "true" : "false")
            << ", \"n\": " << r.n
            << ", \"p50_us\": " << r.latency.p50_us
            << ", \"p95_us\": " << r.latency.p95_us
            << ", \"p99_us\": " << r.latency.p99_us
            << ", \"p999_us\": " << r.latency.p999_us
            << ", \"max_us\": " << r.latency.max_us
            << ", \"avg_us\": " << r.latency.avg_us
            << ", \"ops_per_sec\": " << r.throughput_ops_sec
            << "}";
        if (i + 1 < results.size()) out << ",";
        out << "\n";
    }
    out << "  ]\n}\n";
    return out.str();
}

std::string collect_machine_info() {
    std::ostringstream out;
#ifdef _WIN32
    SYSTEM_INFO si;
    GetSystemInfo(&si);

    // CPU brand via cpuid 0x80000002..04 three reads concatenated.
    // CPU brand 通过 cpuid 0x80000002..04 三次读取拼接。
    int cpu_info[4] = {0};
    char brand[0x40] = {};
    __cpuid(cpu_info, 0x80000000);
    const unsigned int max_ext_id = static_cast<unsigned int>(cpu_info[0]);
    if (max_ext_id >= 0x80000004) {
        __cpuid(reinterpret_cast<int*>(&brand[0]),  0x80000002);
        __cpuid(reinterpret_cast<int*>(&brand[16]), 0x80000003);
        __cpuid(reinterpret_cast<int*>(&brand[32]), 0x80000004);
        // Trim leading whitespace.
        const char* p = brand;
        while (*p == ' ') ++p;
        out << "CPU: " << p << "\n";
    }
    out << "Logical CPUs: " << si.dwNumberOfProcessors << "\n";
    out << "OS: Windows\n";
    out << "Compiler: MSVC _MSC_VER=" << _MSC_VER << "\n";
#else
    out << "OS: Linux\n";
    std::ifstream cpuinfo("/proc/cpuinfo");
    std::string line;
    while (std::getline(cpuinfo, line)) {
        if (line.rfind("model name", 0) == 0) {
            auto pos = line.find(':');
            if (pos != std::string::npos) {
                std::string v = line.substr(pos + 1);
                size_t s = v.find_first_not_of(" \t");
                if (s != std::string::npos) v = v.substr(s);
                out << "CPU: " << v << "\n";
            }
            break;
        }
    }
#ifdef __GNUC__
    out << "Compiler: GCC " << __GNUC__ << "." << __GNUC_MINOR__ << "." << __GNUC_PATCHLEVEL__ << "\n";
#elif defined(__clang__)
    out << "Compiler: Clang " << __clang_major__ << "." << __clang_minor__ << "\n";
#endif
#endif
#ifdef NDEBUG
    out << "Build: Release (NDEBUG)\n";
#else
    out << "Build: Debug\n";
#endif
    return out.str();
}

